export function buildSummarizeChain(): RunnableSequence {
  const llm = new ChatOpenAI({
    model: CHAT_MODEL,
    streaming: true,
    apiKey: process.env.OPENAI_API_KEY,
    temperature: 0.2,
  })

  return RunnableSequence.from([
    SUMMARY_PROMPT,
    llm,
    new StringOutputParser(),
  ])
}
