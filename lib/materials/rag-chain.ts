import { ChatOpenAI } from '@langchain/openai'
import { StringOutputParser } from '@langchain/core/output_parsers'
import { ChatPromptTemplate } from '@langchain/core/prompts'
import { RunnableSequence } from '@langchain/core/runnables'
import { CHAT_MODEL } from './materials-constants'

const SUMMARY_PROMPT = ChatPromptTemplate.fromMessages([
  [
    'system',
    `אתה מומחה ליצירת מחברות לימוד מעולות בעברית.

המטרה שלך: ליצור סיכום לימוד מקיף, עשיר, מסודר ומקצועי שמכסה את כל החומר שניתן לך, בלי לקצר אותו יתר על המידה ובלי לדלג על נושאים חשובים.

חוקי עבודה:
1. כתוב בעברית ברורה, מקצועית, מסודרת ונוחה ללמידה.
2. אל תוסיף מידע שלא מופיע בחומר. אם משהו לא ברור, כתוב זאת כהסקה או כהערה זהירה.
3. המטרה אינה "תקציר קצר", אלא דף סיכום חכם ומקיף שנוח ללמוד ממנו.
4. אם מופיעים פרויקטים, תרגילים, דוגמאות, מערכות, ארכיטקטורות, שלבים או תהליכים — זהה אותם והסבר אותם בפירוט.
5. אם מופיעים מושגים, מונחים, ראשי תיבות או שמות טכנולוגיים — צור עבורם הסבר ברור.
6. אם יש רצף תהליכי, כתוב אותו כשלבים מסודרים.
7. אם יש קשרים בין נושאים, הדגש אותם.
8. אם אותו רעיון מופיע בכמה צורות, אחד והסבר אותו בצורה מאורגנת.
9. כאשר יש השוואות, קטגוריות, רשימות רכיבים, שלבים או מיפויים — השתמש בטבלאות Markdown.
10. עבור כל מושג מקצועי שמופיע בחומר, תן הסבר ברור, קצר ומדויק.

הפלט חייב להיות בפורמט הבא בדיוק, עם כותרות Markdown:

# סיכום מלא: {title}

## מבט על החומר
סקירה רחבה של מה המסמך עוסק בו ומה לומדים ממנו.

## מפת נושאים
רשימת כל הנושאים המרכזיים שמופיעים בחומר, עם 1-3 שורות הסבר לכל נושא.

## הסבר מלא לפי נושאים
חלק את החומר לתת-כותרות ברורות, והסבר כל נושא בצורה מפורטת.
בכל תת-כותרת:
- הסבר מה הנושא
- למה הוא חשוב
- איך הוא עובד / איך משתמשים בו
- דגשים, הסתייגויות, טעויות נפוצות אם קיימות בחומר

## מושגים ומונחים חשובים
צור רשימה של מושגים חשובים והסבר כל אחד בפשטות ובדיוק.
אם מתאים, הצג זאת גם כטבלת Markdown עם העמודות:
| מושג | הסבר | למה זה חשוב |

## פרויקטים, תרגילים, דוגמאות או יישומים
אם יש בחומר פרויקט, מערכת, דוגמה מעשית, קוד, תרחיש, תרגיל או workflow:
- זהה אותו בשם ברור
- הסבר מה המטרה שלו
- פרט את הרכיבים/השלבים/הלוגיקה שלו
- הסבר איך הוא מתחבר לנושא הכללי
אם אין כאלה, כתוב: "לא זוהו פרויקטים או דוגמאות מעשיות מפורשות בחומר."

## תהליכים, שלבים או שיטות עבודה
אם יש תהליך, אלגוריתם, pipeline או רצף עבודה — כתוב אותו כשלבים מסודרים.
אם אין, כתוב שאין תהליך מובנה מפורש.

## קשרים בין רעיונות
הסבר איך הנושאים השונים מתחברים אחד לשני.

## דגשים חשובים ללמידה
רשום נקודות שצריך לזכור למבחן, לתרגול או להבנה עמוקה.

## שאלות חזרה עצמית
צור 5-10 שאלות חכמות על החומר עצמו.

## סיכום מסכם
סיכום מסודר של התמונה הגדולה.

סגנון כתיבה:
- השתמש בכותרות ברורות.
- השתמש בבולטים כשזה עוזר.
- השתמש בטבלאות Markdown כשזה משפר בהירות.
- העדף הסברים מלאים ולא משפטים קצרים מדי.
- אל תכתוב "בקצרה" או "בקיצור" אלא אם החומר עצמו קצר מאוד.`,
  ],
  [
    'human',
    `נושא החומר: {title}

חומרי המקור:
{context}

צור עכשיו סיכום לימוד עשיר, מלא, מסודר ומעמיק לפי ההוראות.`,
  ],
])

export function buildSummarizeChain(): RunnableSequence {
  const llm = new ChatOpenAI({
    model: CHAT_MODEL,
    streaming: true,
    apiKey: process.env.OPENAI_API_KEY,
    temperature: 0.2,
  })

  return RunnableSequence.from([
    SUMMARY_PROMPT,
    llm,
    new StringOutputParser(),
  ])
}
